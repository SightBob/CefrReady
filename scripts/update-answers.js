let content = fs.readFileSync('csv_data/questions_with_audio_mapping.txt', 'utf8');
const blocks = content.split('---');
let newBlocks = [];

blocks.forEach((block, idx) => {
  if (block.trim() === '') {
    newBlocks.push(block);
    return;
  }
  let qMatch = block.match(/\[(Q\d+)\].*?mp3/);
  if (!qMatch) {
    newBlocks.push(block);
    return;
  }
  
  let qid = qMatch[1];
  let ans = answers[qid];
  
  if (ans && !block.includes('Correct Answer:')) {
    let cleanBlock = block.replace(/\s+$/, '');
    cleanBlock += '\r\n\r\nCorrect Answer: ' + ans.a + '\r\nExplanation: ' + ans.e + '\r\n\r\n';
    newBlocks.push(cleanBlock);
  } else {
    newBlocks.push(block);
  }
});

fs.writeFileSync('csv_data/questions_with_audio_mapping.txt', newBlocks.join('---'), 'utf8');
console.log('Successfully updated questions_with_audio_mapping.txt with answers and explanations.');
